def count_hits(stepx, stepy):
    lines = len(input)
    wrap = len(input[0])
    x = 0
    count = 0
    for y in range(0, lines, stepy):
        if input[y][x] == "#":
            count = count + 1
        x = (x + stepx) % wrap
    return count


